public class IncerementOperators {
    public static void main(String args[]){

        int num=5;
        int num2=9;

        System.out.println(num++);         // first prints original then increments
        System.out.println(num);

        System.out.println(num2--);
        System.out.println(num2);

        System.out.println(++num);
        System.out.println(--num2);
    }
}
